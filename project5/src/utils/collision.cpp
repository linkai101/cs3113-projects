#include "utils/collision.h"

bool CheckPointInRect(Vector2 point, const Rectangle& rect) {
  return (
    point.x >= rect.x &&
    point.x <= rect.x + rect.width &&
    point.y >= rect.y &&
    point.y <= rect.y + rect.height
  );
}

bool CheckRectCollision(const Rectangle& a, const Rectangle& b) {
  return (
    a.x < b.x + b.width &&
    a.x + a.width > b.x &&
    a.y < b.y + b.height &&
    a.y + a.height > b.y
  );
}
